    public Mesh GenerateMesh()
    {
        sampler = new GPUSampler(lerpFieldShader, caseNumberShader);

        caseNumbers = sampler.GenerateCaseNumbers();
        collection = sampler.GenerateLerpField();

        listOfVectors = new List<Vector3>();
        listOfTriangles = new List<int>();
        hashedVectors = new Dictionary<Vector3, int>();

        Vector3 position = new Vector3();

        for (int x = 0; x < 16; x++)
        {
            position.x = x;
            for (int y = 0; y < 16; y++)
            {
                position.y = y;
                for (int z = 0; z < 16; z++)
                {
                    position.z = z;

                    March(position);
                }
            }
        }

        Mesh mesh = new Mesh();
        Debug.Log("Number of Vectors : " + listOfVectors.Count);
        Debug.Log("Vectors : " + listOfVectors);
        mesh.SetVertices(listOfVectors);
        Debug.Log("Number of Triangles : " + listOfTriangles.Count);
        Debug.Log("Triangles : " + listOfTriangles);
        mesh.SetTriangles(listOfTriangles, 0);
        mesh.RecalculateNormals();
        mesh.RecalculateBounds();
        mesh.RecalculateTangents();


        return mesh;
    }
    private int index;
    private void March(Vector3 position)
    {
        index = idIndexing(position);
        Debug.Log("Index: " + index);
        if(index == 4067)
        {
            Debug.Log("Close to crash index");
        }
        int caseNumber = (int)caseNumbers[index];
        int numberOfPolygons = case_to_numpolys[caseNumber];
        for (int polygon = 0; polygon < numberOfPolygons; polygon++)
        {
            int a = g_triTable[caseNumber, polygon, 0];
            int b = g_triTable[caseNumber, polygon, 1];
            int c = g_triTable[caseNumber, polygon, 2];

            Vector3[] vec =
            {
                GetVectorFromLerpField(position, a),
                GetVectorFromLerpField(position, b),
                GetVectorFromLerpField(position, c)
            };

            int isInHash;
            for (int v = 0; v < 3; v++)
            {
                if (hashedVectors.TryGetValue(vec[v], out isInHash))
                {
                    listOfTriangles.Add(isInHash);
                }
                else
                {
                    hashedVectors.Add(vec[v], hashedIndex);
                    listOfVectors.Add(vec[v]);
                    listOfTriangles.Add(hashedIndex);
                    hashedIndex++;
                }
            }
        }
    }

